void main() {
 // Operaciones Aritmeritcas
 // +, -, -expr, *, /, ~/ y %
  int valor1;
  int valor2;
  dynamic resultado;
  
  //+
   valor1 = 7;
   valor2 = 3;
   resultado = valor1 + valor2;
  
  print("$valor1 + $valor2 = $resultado");
  
  //-
   valor1 = 7;
   valor2 = 3;
   resultado = valor1 - valor2;
  
  print("$valor1 - $valor2 = $resultado");
  
  //*
   valor1 = 7;
   valor2 = 3;
   resultado = valor1 * valor2;
  
  print("$valor1 * $valor2 = $resultado");
  
  // /
   valor1 = 7;
   valor2 = 3;
   resultado = valor1 / valor2;
  
  print("$valor1 / $valor2 = $resultado");
 
}
